namespace Syphon
{
    /// <summary>
    /// Падежи
    /// </summary>
    internal enum Cases
    {
        /// <summary>
        /// Именительный
        /// </summary>
        Nominative,

        /// <summary>
        /// Винительный
        /// Кого? Что?
        /// </summary>
        Accusative,

        /// <summary>
        /// Родительный
        /// Кого? Чего?
        /// </summary>
        Genitive,

        /// <summary>
        /// Дательный
        /// Кому? Чему?
        /// </summary>
        Dative,

        /// <summary>
        /// Творительный
        /// Кем? Чем?
        /// </summary>
        Ablative,

        /// <summary>
        /// Предложный
        /// О ком? О чём?
        /// </summary>
        Prepositional
    }
}